# Addition
a = 5
b = 3
result_addition = a + b
print("Addition:", result_addition)

# Subtraction
result_subtraction = a - b
print("Subtraction:", result_subtraction)

# Multiplication
result_multiplication = a * b
print("Multiplication:", result_multiplication)

# Division
result_division = a / b
print("Division:", result_division)

# Exponentiation
result_exponentiation = a ** b
print("Exponentiation:", result_exponentiation)





# Equality
x = 10
y = 20
print("Equality:", x == y)

# Inequality
print("Inequality:", x != y)

# Greater than
print("Greater than:", x > y)

# Less than
print("Less than:", x < y)


# AND
condition1 = True
condition2 = False
result_and = condition1 and condition2
print("Logical AND:", result_and)

# OR
result_or = condition1 or condition2
print("Logical OR:", result_or)

# NOT
result_not = not condition1
print("Logical NOT:", result_not)